package com.example.yourptnotebook;

public class Ptrainer {
    String username;
    String email;
    String fullNmame;
    String fullName;
    String gender;
    String gymName;
    String password;
    String id;
    public Ptrainer() {
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public String getFullNmame() {
        return fullNmame;
    }

    public String getFullName() {
        return fullName;
    }

    public String getGender() {
        return gender;
    }

    public String getGymName() {
        return gymName;
    }

    public String getPassword() {
        return password;
    }

    public String getId() {
        return id;
    }
}
